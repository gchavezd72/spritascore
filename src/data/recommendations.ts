      id: `rec-${key}`,
      title: { es: key, en: key },
      description: { es: "", en: "" },
      priority: "media",
      effort: "medio",
      impact: "medio",
      timeframe: "60-dias",
      area: "desarrollo",
      type: "tecnica",
