export function getRecommendationByKey(key: string): Recommendation {
  const template = RECOMMENDATION_TEMPLATES.find((r) => r.key === key);
  if (!template) {
    return {
      id: `rec-${key}`,
      title: { es: key, en: key },
      description: { es: "", en: "" },
      priority: "media",
      effort: "medio",
      impact: "medio",
      timeframe: "60-dias",
      area: "desarrollo",
      type: "tecnica",
    };
  }
  const { key: _key, ...rest } = template;
  return { ...rest, id: `rec-${key}` };
}
