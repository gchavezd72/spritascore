export function getRecommendationByTitle(title: string): Recommendation {
  const template = RECOMMENDATION_TEMPLATES.find((r) => r.title === title);
  if (!template) {
    return {
      id: `rec-${Date.now()}`,
      title,
      description: "",
      priority: "media",
      effort: "medio",
      impact: "medio",
      timeframe: "60-dias",
      area: "desarrollo",
      type: "tecnica",
    };
  }
  return { ...template, id: `rec-${title.slice(0, 20).replace(/\s/g, "-").toLowerCase()}` };
}